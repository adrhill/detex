"""Test asdex sparsity detection against CUTEst ground truth via sif2jax.

These tests compare asdex's detected sparsity patterns against
ground truth from CUTEst benchmark problems.
The goal is to catalog conservativeness — where asdex over-estimates sparsity.

Fixtures are generated by ``tests/setup/generate_cutest_fixtures.py``.

sif2jax problem objects have the following attributes:

- ``name``: str — problem name (e.g. "AKIVA").
- ``y0``: jax.Array — starting point, shape ``(n,)``.
- ``args``: None | tuple of arrays — extra data passed to the objective.
- ``objective(y, args) -> scalar`` — the objective function.
- ``constraint(y) -> (eq_or_none, ineq_or_none)`` — equality and inequality
  constraint vectors (constrained problems only).
"""

