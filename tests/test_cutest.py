def _discover_fixtures(kind: str) -> list[str]:
    """Discover fixture problem names by globbing the fixture directory."""
    subdir = FIXTURE_DIR / kind
    if not subdir.exists():
        return []
    return sorted(p.stem for p in subdir.glob("*.npz"))


_unconstrained_by_name = {
    p.name: p for p in sif2jax.unconstrained_minimisation_problems
}
_constrained_by_name = {p.name: p for p in sif2jax.constrained_minimisation_problems}


def _assert_superset(
    detected: np.ndarray,
    ground_truth: np.ndarray,
    name: str,
    kind: str,
):
    """Assert detected is a superset of ground truth, warn on extras.

    Args:
        detected: Dense boolean array from asdex detection.
        ground_truth: Dense boolean array from ground truth.
        name: Problem name for error messages.
        kind: Pattern kind (hessian, jacobian_eq, jacobian_ineq).
    """
    # Hard assertion: no false negatives.
    missed = ground_truth & ~detected
    assert not missed.any(), (
        f"{kind}/{name}: asdex missed {missed.sum()} nonzeros (false negatives)!\n"
        f"  Ground truth nnz: {ground_truth.sum()}\n"
        f"  Detected nnz: {detected.sum()}\n"
        f"  Missed: {missed.sum()}"
    )

    # Warn on conservativeness (extra nonzeros).
    extras = detected & ~ground_truth
    if extras.any():
        total = detected.shape[0] * detected.shape[1]
        warnings.warn(
            f"{kind}/{name}: asdex detected {extras.sum()} extra nonzeros "
            f"(detected={detected.sum()}, truth={ground_truth.sum()}, "
            f"density: {detected.sum() / total:.1%} vs {ground_truth.sum() / total:.1%})",
            stacklevel=2,
        )


def _check_baseline(
    detected_nnz: int,
    name: str,
    kind: str,
    expected_nnz_dict: dict[str, tuple[int, int]],
):
    """Fail on nnz regressions or improvements so baselines stay current."""
    expected = expected_nnz_dict.get(name)
    if expected is None:
        return
    expected_nnz, _ = expected
    key = f"{kind}/{name}"
    if detected_nnz > expected_nnz:
        pytest.fail(
            f"{key}: REGRESSION — detected nnz increased "
            f"from {expected_nnz} to {detected_nnz}"
        )
    if detected_nnz < expected_nnz:
        pytest.fail(
            f"{key}: IMPROVEMENT — detected nnz decreased "
            f"from {expected_nnz} to {detected_nnz}. "
            f"Update the baseline to ({detected_nnz}, {expected[1]})."
        )


def _objective(problem):
    """Build a scalar function from an unconstrained problem's objective."""
    return lambda y: problem.objective(y, problem.args)


def _eq_constraint(problem):
    """Build a vector function from a constrained problem's equality constraints."""
    return lambda y: problem.constraint(y)[0]


def _ineq_constraint(problem):
    """Build a vector function from a constrained problem's inequality constraints."""
    return lambda y: problem.constraint(y)[1]


def _run_sparsity_test(
    name: str,
    kind: str,
    problems_by_name: dict,
    make_fn,
    detect_fn,
    expected_nnz_dict: dict[str, tuple[int, int]],
):
    """Shared test body for CUTEst sparsity tests.

    Args:
        name: CUTEst problem name.
        kind: Fixture subdirectory (hessian, jacobian_eq, jacobian_ineq).
        problems_by_name: Dict mapping problem names to sif2jax problems.
        make_fn: Callable(problem) -> function under test.
        detect_fn: asdex.hessian_sparsity or asdex.jacobian_sparsity.
        expected_nnz_dict: Baseline dict for this kind.
    """
    if name not in problems_by_name:
        pytest.skip(f"{name} not available in sif2jax")
    problem = problems_by_name[name]

    gt = asdex.SparsityPattern.load(FIXTURE_DIR / kind / f"{name}.npz")

    if problem.y0.size != gt.shape[-1]:
        pytest.skip(
            f"{name}: shape mismatch (sif2jax n={problem.y0.size}, fixture n={gt.shape[-1]})"
        )

    try:
        detected = detect_fn(make_fn(problem), problem.y0.shape)
    except NotImplementedError as exc:
        pytest.xfail(f"asdex internal error for {name}: {exc}")

    _assert_superset(
        detected.todense().astype(bool),
        gt.todense().astype(bool),
        name,
        kind,
    )
    _check_baseline(detected.nnz, name, kind, expected_nnz_dict)


# Hessian tests

hessian_names = _discover_fixtures("hessian")


@pytest.mark.slow
@pytest.mark.cutest
@pytest.mark.hessian
@pytest.mark.parametrize("name", hessian_names)
def test_hessian_sparsity_cutest(name):
    """Compare asdex Hessian sparsity against CUTEst ground truth."""
    _run_sparsity_test(
        name,
        "hessian",
        _unconstrained_by_name,
        _objective,
        asdex.hessian_sparsity,
        EXPECTED_HESSIAN_NNZ,
    )


